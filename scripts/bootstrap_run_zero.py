"""
bootstrap_run_zero.py
---------------------
One-time script that promotes the currently trusted output state into Run 0
(the baseline truth for the run-history subsystem).

Run 0 is:
  - dated 2026-04-10 (the established baseline date for this project)
  - run_type = BASELINE
  - is_baseline = True
  - root_run_number = 0
  - immutable: later runs derive from it

What this script does:
  1. Initialise run_memory.db
  2. Refuse to run again unless --force is passed (idempotency guard)
  3. Create Run 0 record in the DB
  4. Scan existing output/ and output/debug/ artifacts
  5. Copy them into runs/run_0000/ (and runs/run_0000/debug/)
  6. Register each artifact in run_artifacts
  7. Register known input provenance (GED, GF, Mapping)
  8. Mark Run 0 as current
  9. Print a clear summary

Usage:
    cd "GF updater v3"
    python scripts/bootstrap_run_zero.py
    python scripts/bootstrap_run_zero.py --force    # re-run if Run 0 already exists
    python scripts/bootstrap_run_zero.py --db-path data/run_memory.db
    python scripts/bootstrap_run_zero.py --root-dir /path/to/project
"""

